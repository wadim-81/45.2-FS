import "./Lesson04.css"
import { useState } from "react";

function Lesson04() {
  //–≤–æ –≤—Ç–æ—Ä–æ–π —Ç—Ä–µ—Ç–∏ - —Ç–µ–ª–µ —Ñ—É–Ω–∫—Ü–∏–∏  –º—ã —Ä–∞–±–æ—Ç–∞–µ–º —Å –∏–∑–º–µ–Ω –¥–∞–Ω–Ω—ã–º–∏
  //–≤—ã–∑–æ–≤ —Ñ—É–Ω–∫, –∞—Å–∏–Ω—Ö—Ä–æ–Ω–Ω—ã–µ –∑–∞–ø—Ä–æ—Å—ã
  //–≤—ã–∑–æ–≤ —Å–ø–µ—Ü –º–µ—Ç–æ–¥–æ–≤ –≤–Ω—É—Ç—Ä–∏ —Ä–µ–∞–∫—Ç
  //–∫–æ–¥ –Ω–∏–∂–µ —Å—Ä–∞—é–æ—Ç–∞–ª –±—ã –≤ –æ–±—ã—á–Ω–æ–º script.js  –Ω–æ –Ω–µ –æ–±–Ω–∞–≤–ª—è–µ—Ç –¥–∞–Ω–Ω—ã–µ –Ω–∞ —Å—Ä–∞–Ω–∏—Ü–µ —Ä–µ–∞–∫—Ç
  /* let count = 1;
  const handlePLus = () => {
    count++;
    console.log(count);  };*/
  //! –∏–º–µ—Å—Ç–æ –Ω–µ–≥–æ –º—ã –∏—Å–ø —Ñ—É–Ω–∫—Ü–∏—é  useState()
  // –≤ useState –º—ã –ø–µ—Ä–µ–¥–∞–µ–º –Ω–∞—á–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π –∏ –≤ –æ—Ç–≤–µ—Ç –ø–æ–ª—É—á–∞–µ–º
  // –º–∞—Å—Å–∏–≤ –∏–∑ –¥–≤—É—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ : 1 –ø–µ—Ä–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏—è, 2 —Ñ—É–Ω–∫—Ü–∏–∏, –∫–æ—Ç–æ—Ä–æ–π –º—ã —ç—Ç—É –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –ø–µ—Ä–µ–∑–∞–ø–∏—Å—ã–≤–∞–µ–º
  // –¥–ª—è —É–¥–æ–±—Å—Ç–≤–ø –¥–∞–Ω–Ω—ã–µ —Å–æ–±–∏—Ä–∞–µ–º –∏–∑ –º–∞—Å–∏–≤–∞ —á–µ—Ä–µ–∑ –¥–µ—Å—Ç—Ä—É–∫—Ç—É—Ä–∏–∑–∞—Ü–∏—é
  const [count, setCount] = useState(1);
  const handlePlus = () => {
    // —Å –ø–æ–º–æ—â—å—é —Ñ—É–Ω–∫—Ü–∏–∏ –ø—Ä–∏–ª–∞–≥–∞—é—â–µ–π—Å—è –∫ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π –∏–∑–º–µ–Ω—è–µ–º –µ–µ –¥–æ–±–∞–≤–∏–≤ –∫ –ø—Ä–µ–¥—ã–¥—É—â–µ–º—É —Å–æ—Å—Ç–æ—è–Ω–∏—é 1
    setCount((prev) => prev + 1);
  };
  const handleMinus = () => {
    setCount((prev) => prev - 1);
  };
  //—Ä–µ–∞–ª–∏–∑—É–µ–º –∫–Ω–æ–ø–∫—É-–ø–µ—Ä–µ–∫–ª—é—á–∞—Ç–µ–ª—å –¥–ª—è —Ç–æ–≥–æ —á—Ç–æ–±—ã –ø—Ä—è—Ç–∞—Ç—å –∏ –ø–æ–∫–∞–∑—ã–≤–∞—Ç—å —Å—á–µ—Ç—á–∏–∫
  const [isVisible, setIsVisible] = useState(false);
  const handleSwitcher = () => {
    setIsVisible((prev) => !prev);
    
  };
  const [color, setColor] = useState("white");
  const handleRed = () => {
    setColor("Red");
  };
  const handleGreen = () => {
    setColor("Green");
  };
  const handleBlue = () => {
    setColor("Blue");
  };
  return (
    // * –≤ –ø–æ—Å–ª–µ–¥–Ω–µ–π —Ç—Ä–µ—Ç–∏ - –ø–æ—Å–ª–µ return –º—ã –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –≤–µ—Ä—Å—Ç–∫—É –∏ –æ—Ç–æ–±—Ä–∞–∂–∞–µ–º –¥–∞–Ω–Ω—ã–µ –≤ —Ç–µ–≥–∞—Ö
    <div>
      <h2>UseState() hook :haken:</h2>
      <p>
        –ß—Ç–æ–±—ã –ø—Ä–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π, –∫–∞–∫ –≤ –ø—Ä–∏–º–µ—Ä–µ –Ω–∏–∂–µ –º—ã –≤–∏–¥–µ–ª–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∏
        –∫–æ–º–ø–æ–Ω–µ–Ω—Ç –æ–±–Ω–æ–≤–∏–ª—Å—è - –Ω–µ –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –æ–±—ã—á–Ω–æ–π –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π ü´§:
      </p>
      <p>
        –ù—É–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Å–ø–µ—Ü–∏–∞–ª—å–Ω—É—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é —Å–æ—Å—Ç–æ—è–Ω–∏—è –∏–∑ –≤—Å—Ç—Ä–æ–µ–Ω–Ω–æ–π –≤
        React —Ñ—É–Ω–∫—Ü–∏–∏ useState()
      </p>
      <div>
        <button onClick={handleSwitcher}>
          :‚åõ: {!isVisible ? "Show" : "Hide"} counter :‚åõ:
        </button>
      </div>
      {/* –µ—Å–ª–∏ –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π isVisible –±—É–¥–µ—Ç true - –æ—Ç–æ–±—Ä–∞–∑–∏ –¥–∞–Ω–Ω—ã–µ –ø–æ—Å–ª–µ && (–¥–≤–æ–π–Ω–æ–≥–æ –∞–º–ø–µ—Ä—Å–∞–Ω–¥–∞)  */}
      {isVisible && (
        <div className="counter" style={{backgroundColor:color}}>
          <button onClick={handleRed } className="Red">Red</button>
          <button onClick={handleGreen } className="Green">Green</button>
          <button onClick={handleBlue } className="Blue">Blue</button>
          <p>React Counter with useState hook:</p>
          <button onClick={handleMinus}>-</button>
          <span>{count}</span>
          <button onClick={handlePlus}>+</button>
        </div>
      )
      }
    </div>
  );
}

export default Lesson04;




